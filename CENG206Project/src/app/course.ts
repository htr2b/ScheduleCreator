export class Course {
    code: any;
    name: any;
    year: any;
    credit: any;
    core: any;
    dors: any;
    nofstudent: any;
    instructor: any;
    hours: any;

    constructor(code: any, name: any, year: any, credit: any, core: any, dors: any, nofstudent: any, instructor: any, hours: any) {
        this.code = code;
        this.name = name;
        this.year = year;
        this.credit = credit;
        this.core = core;
        this.dors = dors;
        this.nofstudent = nofstudent;
        this.instructor = instructor;
        this.hours = hours;
    }

}
